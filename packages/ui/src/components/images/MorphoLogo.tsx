export function MorphoLogo({ size = '5' }: { size?: string }) {
  const [mounted, setMounted] = useState(false)
  const { resolvedTheme } = useTheme()

  useEffect(() => {
    setMounted(true)
  }, [])

  // Show a placeholder during hydration to prevent mismatch
  if (!mounted) {
    return (
      <div className={`relative h-${size} w-auto`}>
        <LogoDarkSvg />
      </div>
    )
  }

  // After mounting, use resolvedTheme for accurate theme detection
  return (
    <div className={`relative h-${size} w-auto`}>
      {resolvedTheme === 'dark' ? <LogoDarkSvg /> : <LogoLightSvg />}
    </div>
  )
}
